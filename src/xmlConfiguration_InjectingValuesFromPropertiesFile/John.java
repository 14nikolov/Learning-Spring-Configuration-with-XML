package xmlConfiguration_InjectingValuesFromPropertiesFile;

public class John {

    // Fields

    String firstName;
    int age;

    // Setters

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public void setAge(int age) {
        this.age = age;
    }

    // Getters

    public String getFirstName() {
        return firstName;
    }

    public int getAge() {
        return age;
    }
}
